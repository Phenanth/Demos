#include <iostream>
using namespace std;

int main(){
	int a, b;
	cin >> a >> b;
	if(a > b)
		cout << "true" << endl;
	else cout << "false" << endl;
	if(a < b)
		cout << "true" << endl;
	else cout << "false" << endl;
	if(a == b)
		cout << "true" << endl;
	else cout << "false" << endl;
	if(a != b)
		cout << "true";
	else cout << "false";
	return 0;
}
